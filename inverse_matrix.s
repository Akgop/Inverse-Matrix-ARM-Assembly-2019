; ### Initialize Part ###
;=============== Register Map ================
; r0  = Matrix_Size N
; r1  = const 1
; r2  = const 0
; r9  = outer loop iteration counter
; r10 = inner loop iteration counter
; r11 = Result_data
; r12 = Matrix_data
;=============================================
	LDR r12, =Matrix_data	;r12 = Data
	LDR r11, Result_data
	LDR r0, [r12], #4		;r0 = Matrix size N;
	CMP r0, #1
	BEQ SIZE_ONE
	SUB r9, r0, #1 
	MOV r1, #0x3F800000		;r1 = 1
	STR r1, [r11], #4
	MOV r10, r0
Initialize_Result
	STR r2, [r11], #4
	SUBS r10, r10, #1
	BNE Initialize_Result
	STREQ r1, [r11], #4
	MOVEQ r10, r0
	SUBEQS r9, r9, #1
	BNE Initialize_Result
	
; ### Gauss Elimination ###
;=============== Register Map ================
; r0(x)	= Matrix_Size N
; r1(x)	= Parameter float 1
; r2(x)	= Parameter float 2
; r3	= Matrix Data Pivot Below Address (to get const K)
; r4	= Matrix Data Pivot Address (to get const K)
; r5(x)	= Return Value of each Calculation result
; r6	= Result Data Pivot Below
; r7	= Matirx Data Pivot Below
; r8(x)	= Row Function Iterator
; r9(x)	= Inner Loop Iterator
; r10(x)= Outer Loop Iterator      & temporary space for R5(calculation result)
; r11(x)= Result Data Address (Result Data Pivot)
; r12(x)= Matrix Data Address (Matrix Data Pivot)
; r13	= Stack Pointer(sp)
;============================================
; ### Main Function ###	
Main
	LDR sp, Stack_Address
	STMIA sp!, {r12}
	LDR r11, Result_data
	MOV r4, r12					;pivot
	ADD r7, r12, r0, LSL #2
	MOV r10, r0
Lower_Triangle_OuterLoop		;for int i = N; i-- i > 0;
	SUBS r10, r10, #1			;i++
	BEQ Diagonal
	ADD r9, r10, #1					;int j = i;
	ADD r6, r11, r0, LSL #2
	STMIA sp!, {r10}
	ADD r3, r4, r0, LSL #2		;pivot below
	LDR r2, [r4]
	STMIA sp!, {r2,r11,r12}
Lower_Triangle_InnerLoop		;for int j = i; j < N; j++
	SUBS r9, r9, #1				;j < N ?
	BEQ Next_row
	LDR r2, [r4]
	MOV r8, r0
	CMP r2, #0
	BEQ Row_func2
	LDR r1, [r3]
	STMIA sp!, {r3-r4, r6-r8}
	BL Tokenize_DIV				;r1 = matrix_data pivot, r2 = sub node data
	MOV r10, r5
Row_func3
;======== Matrix Data ========
	LDR r1, [r12], #4
	MOV r2, r10					;DIV value x Matrix_data
	CMP r1, #0
	MOVEQ r5, #0
	STMIANE sp!, {r3-r4, r6-r8}
	BLNE Tokenize_MUL
	LDR r1, [r7]
	MOVS r2, r5
	ADDNE r2, r2, #2147483648
	MOVEQ r2, #0
	STMIA sp!, {r3-r4, r6-r8}
	BL Tokenize_Add_Sub
	CMP r7, r3
	MOVEQ r5, #0
	STR r5, [r7], #4
;======== Result Matrix ========	
	LDR r1, [r11], #4
	MOV r2, r10					;r1 = Result_data, r2 = const k
	CMP r1, #0
	MOVEQ r5, #0
	STMIANE sp!, {r3-r4, r6-r8}
	BLNE Tokenize_MUL
	LDR r1, [r6]	;r1 = sub Result_data, r2 = const k * pivot
	MOVS r2, r5
	ADDNE r2, r2, #2147483648
	MOVEQ r2, #0
	STMIA sp!, {r3-r4, r6-r8}
	BL Tokenize_Add_Sub
	STR r5, [r6], #4
	SUBS r8, r8, #1
	BNE Row_func3
	ADD r3, r3, r0, LSL #2
	LDMDB sp, {r2,r11,r12}
	B Lower_Triangle_InnerLoop	;row function end

Row_func2
;=========== Matrix data ===========
	LDR r1, [r12]
	SWP r1, r1, [r7]
	ADD r7, r7, #4
	STR r1, [r12], #4
;=========== Result data ============
	LDR r1, [r11]
	SWP r1, r1, [r6]
	ADD r6, r6, #4
	STR r1, [r11], #4
	SUBS r8, r8, #1
	BNE Row_func2
	ADD r3, r3, r0, LSL #2
	LDMDB sp, {r2,r11,r12}
	B Lower_Triangle_InnerLoop	;row function end

Next_row
	SUB sp, #12
	ADD r4, r4, r0, LSL #2
	MOV r12, r4
	ADD r7, r12, r0, LSL #2
	ADD r4, r4, #4
	ADD r11, r11, r0, LSL #2
	LDMDB sp!, {r10}
	B Lower_Triangle_OuterLoop

;================ Register Map ====================
; r0	= Matrix_Size N
; r1	= parameter float1(const 1)
; r2	= parameter float2
; r3	= constant 1: 0x3F800000
; r4	= pivot
; r5	= calculate result (return value)
; r6	= DIV value
; r8	= Row Function Iterator
; r9	= Inner Loop Iterator
; r11	= Result Data Address (Result Data Pivot)
; r12	= Matrix Data Address (Matrix Data Pivot)
; r13	= Stack Pointer(sp)
;==================================================
Diagonal
	LDR r11, Result_data
	LDMDB sp!, {r12}
	MOV r4, r12
	MOV r3, #0x3F800000
	ADD r8, r0, #1
Diagonal_Outer
	SUBS r8, r8, #1
	BEQ Upper_Triangle
	LDR r2, [r4]
	MOV r9, r0
Diagonal_Inner
;====== Matrix Data ======	
	LDR r1, [r12]
	STMIA sp!, {r3-r4, r6-r8}
	BL Tokenize_DIV
	CMP r4, r12
	MOVEQ r5, r3
	STR r5, [r12], #4
;====== Result Data ======	
	LDR r1, [r11]
	STMIA sp!, {r3-r4, r6-r8}
	BL Tokenize_DIV
	STR r5, [r11], #4
	SUBS r9, r9, #1
	BEQ return_diagonal
	BNE Diagonal_Inner

return_diagonal
	ADD r4, r4, r0, LSL #2
	ADD r4, r4, #4
	B Diagonal_Outer
; ### Jordan Elimination ###
;=============== Register Map ================
; r0(x)	= Matrix_Size N
; r1(x)	= parameter float1
; r2(x)	= parameter float2
; r3	= Matrix Data Pivot Up Address (to get const K)
; r4	= Matrix Data Pivot Address (to get const K)
; r5(x)	= Return Value of each Calculation result
; r6	= Result Data Pivot Up
; r7	= Matirx Data Pivot Up
; r8(x)	= Row Function Iterator
; r9(x)	= Inner Loop Iterator
; r10(x)= Outer Loop Iterator      & temporary space for R5(calculation result)
; r11(x)= Result Data Address (Result Data Pivot)
; r12(x)= Matrix Data Address (Matrix Data Pivot)
; r13	= Stack Pointer(sp)
;============================================
Upper_Triangle
	SUB r12, r12, #4
	SUB r11, r11, #4
	SUB r7, r12, r0, LSL #2
	MOV r4, r12
	MOV r10, r0
Upper_Triangle_OuterLoop	
	SUBS r10, r10, #1
	BEQ Finish
	ADD r9, r10, #1
	SUB r6, r11, r0, LSL #2
	STMIA sp!, {r10}
	SUB r3, r4, r0, LSL #2		;pivot up
	LDR r2, [r4]
	STMIA sp!, {r2, r11, r12}
Upper_Triangle_InnerLoop		
	SUBS r9, r9, #1
	BEQ Prev_row
	LDR r2, [r4]
	LDR r1, [r3]
	STMIA sp!, {r3-r4, r6-r8}
	BL Tokenize_DIV
	MOV r10, r5
	MOV r8, r0
Reverse_row_func3
;======== Matrix Data =========
	LDR r1, [r12], #-4
	MOV r2, r10
	CMP r1, #0
	MOVEQ r5, #0
	STMIANE sp!, {r3-r4, r6-r8}
	BLNE Tokenize_MUL
	LDR r1, [r7]
	MOVS r2, r5
	ADDNE r2, r2, #2147483648
	MOVEQ r2, #0
	STMIA sp!, {r3-r4, r6-r8}
	BL Tokenize_Add_Sub
	CMP r7, r3
	MOVEQ r5, #0
	STR r5, [r7], #-4
;======== Result Matrix ========	
	LDR r1, [r11], #-4
	MOV r2, r10					;r1 = Result_data, r2 = const k
	CMP r1, #0
	MOVEQ r5, #0
	STMIANE sp!, {r3-r4, r6-r8}
	BLNE Tokenize_MUL
	LDR r1, [r6]	;r1 = sub Result_data, r2 = const k * pivot
	MOVS r2, r5
	ADDNE r2, r2, #2147483648
	MOVEQ r2, #0
	STMIA sp!, {r3-r4, r6-r8}
	BL Tokenize_Add_Sub
	STR r5, [r6], #-4	;push data
	SUBS r8, r8, #1
	BNE Reverse_row_func3
	SUB r3, r3, r0, LSL #2
	LDMDB sp, {r2,r11,r12}
	B Upper_Triangle_InnerLoop

Prev_row
	SUB sp, #12
	SUB r4, r4, r0, LSL #2
	MOV r12, r4
	SUB r7, r12, r0, LSL #2
	SUB r4, r4, #4
	SUB r11, r11, r0, LSL #2
	LDMDB sp!, {r10}
	B Upper_Triangle_OuterLoop
	
SIZE_ONE
	MOV r1, #0x3F800000
	LDR r2, [r12]
	BL Tokenize_DIV
	STR r5, [r11]
	
	
Finish
	MOV pc, #0		;program end
	
; ### Multiplication ###
;=========== Register Map ================
; r1 = parameter A
; r2 = parameter X
; r3 = 1A
; r4 = 2A
; r5 = float1 Mantissa (Multiplicand A) -> final result
; r6 = float2 Mantissa (Multiplier X)
; r7 = 3bit of X
; r8 = A x X temporary result
;=========================================

return0
	MOV r5, #0
	LDMDB sp!, {r3-r4, r6-r8}
	MOV pc, lr

Tokenize_MUL
	CMP r2, #0
	BEQ return0
	;mantissa
	MOV r5, r1, LSL #9;
	MOV r5, r5, LSR #9;		;r5 = F1
	MOV r6, r2, LSL #9;
	MOV r6, r6, LSR #9		;r6 = F2
	;add 1 to mantissa
	ADD r5, r5, #8388608;	;r5 = M1 -> 24bit
	ADD r6, r6, #8388608;	;r6 = M1 -> 24bit
	MOV r6, r6, LSL #1		;LSB as 0 r6 = X 13bit
	MOV r8, #0
	MOV r3, #14

;=========== Booth-4 Multiplication <float1 x float2>============
Booth4	;r8 = result count1
	SUBS r3, r3, #1
	BEQ Normalize_MUL
	MOV r7, r6, LSL #29		
	MOVS r7, r7, LSR #29		;Y = 3bit
	BEQ ShiftOnly
	CMP r7, #2
	BLE AddA
	CMP r7, #4
	BLT Add2A
	BEQ Sub2A
	CMP r7, #6
	BLE SubA
	BGT ShiftOnly	
ShiftOnly
	MOV r8, r8, ASR #2
	MOV r6, r6, LSR #2
	B Booth4
AddA
	ADD r8, r8, r5, LSL #6		;result + A
	MOV r8, r8, ASR #2	;shift 2bit
	MOV r6, r6, LSR #2
	B Booth4
SubA
	SUB r8, r8, r5, LSL #6
	MOV r8, r8, ASR #2	;shift 2bit
	MOV r6, r6, LSR #2
	B Booth4
Add2A
	ADD r8, r8, r5, LSL #7		;result + A
	MOV r8, r8, ASR #2	;shift 2bit
	MOV r6, r6, LSR #2
	B Booth4
Sub2A
	SUB r8, r8, r5, LSL #7		;result + A
	MOV r8, r8, ASR #2	;shift 2bit
	MOV r6, r6, LSR #2
	B Booth4
Normalize_MUL
	;sign bit
	MOV r3, r1, LSR #31;	;r3 = S1
	EOR r3, r3, r2, LSR #31
	;exponent
	MOV r4, r1, LSL #1;
	MOV r4, r4, LSR #24;	;r5 = E1
	MOV r5, r2, LSL #1
	ADD r4, r4, r5, LSR #24
	SUB r4, r4, #127		;r4 = result exponent
	
	CMP r8, #134217728		;2^28
	MOVGT r8, r8, LSR #1
	ADDGT r4, r4, #1
	
	MOV r7, r8, LSL #30
	MOVS r7, r7, LSR #31
	
	MOV r8, r8, LSL #6
	MOV r5, r8, LSR #9
	ADDNE r5, r5, #1
Store_MUL
	ADD r5, r5, r3, LSL #31
	ADD r5, r5, r4, LSL #23
	LDMDB sp!, {r3-r4, r6-r8}
	MOV pc, lr
	
; ### Division ###
