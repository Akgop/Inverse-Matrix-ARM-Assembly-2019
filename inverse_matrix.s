	MOV r3, r1, LSR #31;	;r3 = S1
	EOR r3, r3, r2, LSR #31		;r3 = result sign bit
	;exponent
	MOV r4, r1, LSL #1;
	MOV r4, r4, LSR #24;	;r5 = E1
	MOV r5, r2, LSL #1
	SUB r4, r4, r5, LSR #24
