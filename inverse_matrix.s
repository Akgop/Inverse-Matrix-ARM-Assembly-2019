; ### Addition & Subtraction ###
;============ Register Map =============
; r1 = parameter1
; r2 = parameter2
; r3 = exponent1	-> exponent
; r4 = exponent2	-> sign bit float1
; r5 = mantissa1	-> result
; r6 = mantissa2
; r7 = shift amount	-> sign bit float2
; r8 = temporary result
;=======================================
Tokenize_Add_Sub
	CMP r1, #0
	CMPEQ r2, #0
	BEQ return0
	;exponent
	MOV r3, r1, LSL #1;
	MOV r3, r3, LSR #24;	;r3 = E1
	MOV r4, r2, LSL #1;
	MOV r4, r4, LSR #24;	;r4 = E2
	;mantissa
	MOV r5, r1, LSL #9;
	MOV r5, r5, LSR #9;		;r5 = F1
	MOV r6, r2, LSL #9;
	MOV r6, r6, LSR #9;		;r6 = F2
	;add 1 to mantissa
	ADD r5, r5, #8388608;	;r5 = M1
	ADD r6, r6, #8388608;	;r6 = M1

	CMP r3, r4				;r9 = shift amount
	SUBGT r7, r3, r4		;if E1 > E2
	MOVGT r6, r6, LSR r7	;M2 >> r7(shift amount) 	
	SUBLT r7, r4, r3		;else if E1 < E2
	MOVLT r5, r5, LSR r7;	;M1 >> r7(shift amount)
	;exponent normalize
	MOVLE r3, r4			;r3 = exponent
	;compare sign bit
	;sign bit
	MOV r4, r1, LSR #31;	;r3 = S1
	MOV r7, r2, LSR #31;	;r4 = S2
	CMP r4, r7
	BNE NEADD					;P+N or N+P
;=================== Add & Sub <float1 + float2> =================
EQADD
	ADD r5, r5, r6			;r7 = sum of mantissa
	;normalize result
	CMP r5, #16777216		;exponent == 10(2) ?
	MOVGE r5, r5, LSR #1	;if E >= 10, then shift 1bit
	ADDGE r3, r3, #1		;exponent++
	B Normalize_Add_Sub
NEADD
	CMP r5, r6				;r7 = sum of mantissa
	MOVLT r4, r7			;r7 < r8 -> sign bit changes to r4
	SUBGT r5, r5, r6		;r7 > r8 -> r7 = r7-r8
	SUBLT r5, r6, r5		;r7 < r8 -> r7 = r8-r7
	BEQ return0
loop						;normalize result
	CMP r5, #8388608
	SUBLT r3, r3, #1		;exponent--
	MOVLT r5, r5, LSL #1	;left shift mantissa
	BLT loop
	;change sign bit	
Normalize_Add_Sub		;r4 = Sign bit, r3 = Exponent, r5 = Mantissa
	SUB r5, r5, #8388608
	ADD r5, r5, r4, LSL #31
	ADD r5, r5, r3, LSL #23
	LDMDB sp!, {r3-r4, r6-r8}
	MOV pc, lr

